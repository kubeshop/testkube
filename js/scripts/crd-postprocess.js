const fs = require("fs");
const path = require("path");

// This script should be run after controller-gen.
// It reads CRD YAML files from k8s/crd and emits a shared Helm library template
// that both the operator and runner charts can include conditionally.
const src = "k8s/crd";
const dst = "k8s/helm/testkube-crds/templates/_generated_crds.tpl";
const header = "{{/* Generated by js/scripts/crd-postprocess.js. Do not edit manually. */}}\n";

function templateNameFor(file) {
  return `testkube-crds.item.${file}`;
}

function renderDefineBlock(file, content) {
  return `{{- define "${templateNameFor(file)}" -}}\n${content}\n{{- end }}\n`;
}

fs.readdir(src, (err, files) => {
  if (err) throw err;

  const sortedFiles = files.filter((file) => file.endsWith(".yaml")).sort();
  const sections = [header];

  sections.push('{{- define "testkube-crds.render" -}}\n');
  for (const file of sortedFiles) {
    sections.push(`{{ include "${templateNameFor(file)}" . }}\n`);
  }
  sections.push("{{- end }}\n");

  for (const file of sortedFiles) {
    const content = fs.readFileSync(path.join(src, file), "utf8").trimEnd();
    sections.push(renderDefineBlock(file, content));
  }

  fs.mkdirSync(path.dirname(dst), { recursive: true });
  fs.writeFileSync(dst, sections.join("\n"));
});
